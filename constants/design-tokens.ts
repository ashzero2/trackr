export type ColorSchemeName = 'light' | 'dark';

export function getSemanticColors(scheme: ColorSchemeName) {
  const p = scheme === 'dark' ? paletteDark : palette;
  return {
    ...p,
    /** Solid panel for tab bar (no shadow; flat) */
    chromeSurface: p.surfaceContainerLow,
    /** App header background */
    headerGlassFallback:
      scheme === 'dark' ? 'rgba(15, 23, 42, 0.94)' : 'rgba(246, 250, 254, 0.96)',
    tabBarInactive: scheme === 'dark' ? '#94a3b8' : '#64748b',
    tabBarActiveBg: scheme === 'dark' ? '#34d399' : p.primary,
    tabBarActiveFg: scheme === 'dark' ? '#022c22' : p.onPrimary,
  };
}

export type SemanticColors = ReturnType<typeof getSemanticColors>;
